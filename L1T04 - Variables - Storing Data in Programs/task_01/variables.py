# Once we have defined each variable type we will print them out to the terminal. I've also added in an additional print to showcase the type.

# Variable integer
my_int=7
print(my_int)
print(type(my_int))

# Variable float
my_float=13.56
print(my_float)
print(type(my_float))

# Variable string
my_string="Hi, I am a string."
print(my_string)
print(type(my_string))

# Variable boolean
my_boolean=True
print(my_boolean)
print(type(my_boolean))

